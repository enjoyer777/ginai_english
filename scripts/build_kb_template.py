def main() -> None:
    wb = Workbook()
    wb.remove(wb.active)

    for sheet_name, rows in [
        ("Курсы", COURSES),
        ("Расписание", SCHEDULES),
        ("Преподаватели", TEACHERS),
        ("FAQ", FAQ),
        ("Настройки", SETTINGS),
    ]:
        ws = wb.create_sheet(sheet_name)
        for row in rows:
            ws.append(row)
        # Автоширина по содержимому первой строки + чуть
        for i, header in enumerate(rows[0], start=1):
            ws.column_dimensions[ws.cell(1, i).column_letter].width = max(
                14, min(60, len(str(header)) + 4)
            )

    OUT.parent.mkdir(parents=True, exist_ok=True)
    wb.save(OUT)
    print(f"Saved {OUT}")


if __name__ == "__main__":
    main()
